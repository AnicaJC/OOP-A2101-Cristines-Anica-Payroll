    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        try {
            String input = txtHoursWorked.getText().trim();
            if (input.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter hours worked.");
                return;
            }

            double hours = Double.parseDouble(input);
            updateAllPayrollLabels(hours);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid input. Please enter a numeric value for hours.");
        }
    }//GEN-LAST:event_btnCalculateActionPerformed

    private void updateAllPayrollLabels(double hours) {
        SalaryService salaryService = new SalaryService();

        double netPay = salaryService.calculateWeeklyNet(currentUser, hours);
        double rate = currentUser.getHourlyRate();
        double grossPay = rate * hours;

        double sss = salaryService.calculateSSS(currentUser.getBasicSalary()) / 4;
        double ph = salaryService.calculatePhilhealth(currentUser.getBasicSalary()) / 4;
        double pi = 100.00 / 4;

        double taxableIncome = grossPay - (sss + ph + pi);
        double tax = salaryService.calculateWithholdingTax(taxableIncome);
        double totalDeductions = sss + ph + pi + tax;

        lblGrossPay.setText(String.format("Gross Pay: PHP %,.2f", grossPay));
        lblSSS.setText(String.format("SSS: PHP %,.2f", sss));
        lblPhilHealth.setText(String.format("PhilHealth: PHP %,.2f", ph));
        lblPagibig.setText(String.format("Pag-ibig: PHP %,.2f", pi));
        lblTax.setText(String.format("Tax: PHP %,.2f", tax));
        lblTotalDeductions.setText(String.format("Total Deductions: PHP %,.2f", totalDeductions));
        lblNetPay.setText(String.format("Net Pay: PHP %,.2f", netPay));

        this.revalidate();
        this.repaint();
    }

    public void setHoursAndCalculate(double hours) {
        if (txtHoursWorked != null) {
            if (txtHoursWorked.getText().isEmpty()) {
                txtHoursWorked.setText(String.format("%.2f", hours));
                updateAllPayrollLabels(hours);
            }
        }
    }

