    public void performCalculations(double hours) {
        try {
            double rate = currentUser.getHourlyRate();
            double gross = rate * hours;

            double sss = com.motorph.service.SalaryCalculator.calculateSSS(gross);
            double ph = com.motorph.service.SalaryCalculator.calculatePhilHealth(gross);
            double pi = com.motorph.service.SalaryCalculator.calculatePagIBIG(gross);

            double taxableIncome = gross - (sss + ph + pi);
            double tax = com.motorph.service.SalaryCalculator.calculateTax(taxableIncome);
            double net = gross - (sss + ph + pi + tax);

            lblGrossPay.setText(String.format("Gross: PHP %,.2f", gross));
            lblSSS.setText(String.format("SSS: PHP %,.2f", sss));
            lblPhilHealth.setText(String.format("PhilHealth: PHP %,.2f", ph));
            lblPagibig.setText(String.format("Pag-ibig: PHP %,.2f", pi));
            lblTax.setText(String.format("Tax: PHP %,.2f", tax));
            lblNetPay.setText(String.format("Net Pay: PHP %,.2f", net));

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error calculating salary: " + e.getMessage());
        }
    }

